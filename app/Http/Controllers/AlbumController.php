     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $albums = new Album();

        $albums->codigo = $request->get("codigo");
        $albums->nombre = $request->get("nombre");
        $albums->descripcion = $request->get("descripcion");
        $albums->cantidad = $request->get("cantidad");
        $albums->precio = $request->get("precio");

        $albums->save();

        return redirect('/albums');
        
    }

